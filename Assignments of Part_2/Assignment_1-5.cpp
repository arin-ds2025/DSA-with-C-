// Wap to find the circumference of a circle with radius 10.

#include <iostream>
using namespace std;

int main(){
    // Circumference of a circle = 2*pi*radius
    const float pi = 3.1416;
    float radius = 10.0;
    cout<<"Circumference of circle with radius "<<radius<<" is "<<2*pi*radius<<endl;
}